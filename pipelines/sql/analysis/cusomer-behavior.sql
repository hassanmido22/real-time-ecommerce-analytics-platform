-- 4.  What percentage of customers never received their order on time?

select sum(case when order_delivered_on_time = 0 then 1 else 0 end )*100.0/COUNT(customer_id) as never_on_time_pct
from(
select customers.customer_id, sum(case when 
    CAST(order_delivered_customer_date AS TIMESTAMP) > 
    CAST(order_estimated_delivery_date AS TIMESTAMP) then 0 else 1 end) as order_delivered_on_time from orders
    join customers on orders.customer_id = customers.customer_id
    where order_status = 'delivered'AND order_delivered_customer_date IS NOT NULL
AND order_estimated_delivery_date IS NOT NULL

    group by customers.customer_id)

-- added the condition to only include delivered orders with valid delivery dates to ensure that the analysis accurately reflects the percentage of customers who never received their orders on time, based on reliable data.
-- also added the condition to check for non-null delivery dates to avoid skewing the results with incomplete data, which could lead to an inaccurate representation of customer delivery experiences.
