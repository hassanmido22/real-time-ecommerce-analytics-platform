-- 5.  Which customer state generates the most revenue?
SELECT * FROM (
    SELECT 
        c.customer_state,
        ROUND(sum(order_total), 2) AS sum_order_value
    FROM orders o
    JOIN customers c ON c.customer_id = o.customer_id
    JOIN (
        SELECT order_id, SUM(price) AS order_total
        FROM order_items
        GROUP BY order_id
    ) AS order_totals ON order_totals.order_id = o.order_id
    GROUP BY c.customer_state
) AS city_orders
ORDER BY sum_order_value DESC
